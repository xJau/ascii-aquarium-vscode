// Eye highlight masks: W = bright white
// dir 0 (right): eye is on the right side
const MONSTER_RIGHT_MASK =
  "                                                W W\n" +
  "\n" +
  "\n" +
  "";

// dir 1 (left): eye is on the left side
const MONSTER_LEFT_MASK =
  "   W W\n" +
  "\n" +
  "\n" +
  "";

export function addMonster(world: World, rng: () => number = Math.random): Entity {
  const goRight = rng() < 0.5;
  const speed = 2;

  const frameList = goRight ? MONSTER_RIGHT_FRAMES : MONSTER_LEFT_FRAMES;
  const maskStr = goRight ? MONSTER_RIGHT_MASK : MONSTER_LEFT_MASK;

  // Masks array must have same length as frames. Use "" to avoid validation issues
  // (mask only covers the top row with the eye; frame has leading blank line)
  const masks = frameList.map(() => "");

  // ~54 cols wide (right), ~54 (left)
  const frameWidth = 54;
  const x = goRight ? -frameWidth : world.width;

  const numFrames = frameList.length;

  return world.add({
    name: "monster",
    type: "event",
    x,
    y: 2,
    z: 3,
    defaultColor: "G",
    dieOffscreen: true,
    shape: {
      frames: frameList,
      masks,
    },
    callback: (e, dt) => ({
      dx: (goRight ? 1 : -1) * speed * dt * 8,
      frame: Math.floor(e.age * 4) % numFrames,
    }),
  });
}
